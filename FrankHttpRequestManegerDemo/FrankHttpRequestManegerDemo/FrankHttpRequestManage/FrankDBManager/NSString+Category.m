+ (NSString*)safeGetValue:(id)string
{
    id ret = string ? string : @"";
    
    if([ret isKindOfClass:[NSNumber class]])
    {
        ret = [NSString stringWithFormat:@"%ld", (long)[ret integerValue]];
    }
    else if([ret isKindOfClass:[NSNull class]])
    {
        ret = @"";
    }
    else
    {
        ret = [(NSString *)ret stringByReplacingOccurrencesOfString:@"\\r\\n" withString:@"\r\n"];
    }
    
    return  ret;
}

