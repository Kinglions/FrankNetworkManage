+(NSMutableAttributedString *)getContent:(NSString *)str withParagrahStyleAlignment:(NSTextAlignment)Alignment withLineSpacing:(CGFloat)lineSpacing withFirstLingHeadIndent:(CGFloat)firstLineHeadindent;
{
    if (!str)
    {
        return nil;
    }
    
    if (!firstLineHeadindent)
    {
        firstLineHeadindent = 0;
    }
    
    NSMutableAttributedString *strAttribute = [[NSMutableAttributedString alloc] initWithString:str];
    
    NSMutableParagraphStyle *paragraphStyle = [[NSMutableParagraphStyle alloc ] init ];
    
    paragraphStyle.alignment = Alignment;
    
    paragraphStyle.lineSpacing = lineSpacing;  //行自定义行高度
    
    [paragraphStyle setFirstLineHeadIndent: firstLineHeadindent ]; //首行缩进 根据用户昵称宽度在加5个像素
    
    [strAttribute addAttribute : NSParagraphStyleAttributeName value :paragraphStyle range : NSMakeRange ( 0 , [str length])];
    
    return strAttribute;
}

