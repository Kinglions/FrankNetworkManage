- (BOOL)matchString:(NSString *)input withPattern:(NSString *)pattern
{
    if (!input || 0 == [input length]) {
        return FALSE;
    }
    NSPredicate *predicate = [NSPredicate predicateWithFormat:@"SELF MATCHES %@", pattern];
    return [predicate evaluateWithObject:input];
}

- (BOOL)containString:(NSString *)input
{
    if (!input || 0 == [input length]) {
        return FALSE;
    }
    NSPredicate *predicate = [NSPredicate predicateWithFormat:@"SELF CONTAINS %@", input];
    return [predicate evaluateWithObject:self];
}


