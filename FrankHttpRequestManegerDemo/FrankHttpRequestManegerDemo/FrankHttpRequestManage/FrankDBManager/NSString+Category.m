-(NSString *)changeStringToPinyin{
    
    // 转换成 C 语言
    CFStringRef strRef = (__bridge CFStringRef)self;
    //第一个参数是内存分配器 NULL 默认系统提供的分配器   // 第二个参数定义当前字符串的长度 如果是0则表示无限制//    第三个参数就是得到的不可变字符串
    CFMutableStringRef mutableStrRef = CFStringCreateMutableCopy(NULL, 0, strRef);
    //第一参数：得到的可变字符串// 第二个参数：转化的范围 一般NULL// 第三个参数是转化规则//第四个参数就是是否字符串反转
    CFStringTransform(mutableStrRef, NULL, kCFStringTransformMandarinLatin, NO);
    // 去掉声调
    CFStringTransform(mutableStrRef, NULL, kCFStringTransformStripDiacritics, NO);

    // 转换成 NSString
    NSString *newStr = (__bridge NSString *) mutableStrRef;
    // 去除空格
    newStr = [newStr replaceStrWithChatStrArr:@[@" "] toTheStr:@""];
