// MD5 加密
- (id)MD5
{
    const char *cStr = [self UTF8String];
    unsigned char digest[16];
    CC_MD5( cStr, strlen(cStr), digest ); // This is the md5 call
    
    NSMutableString *output = [NSMutableString stringWithCapacity:CC_MD5_DIGEST_LENGTH * 2];
    
    for(int i = 0; i < CC_MD5_DIGEST_LENGTH; i++)
        [output appendFormat:@"%02x", digest[i]];
    
    return  output;
}




// 获取路径的一个类别方法
+(NSString *)getFilePathWithDirectoryPath:(NSString *)aDirectoryPath FileName:(NSString *)aFileName
{
    if (aDirectoryPath == nil)
    {
        aDirectoryPath = @"Documents";
    }
    
    NSString * DirectoryPath = [NSHomeDirectory() stringByAppendingPathComponent:[NSString stringWithFormat:@"%@",aDirectoryPath]];
    
    NSFileManager * manage = [NSFileManager defaultManager];
    
    if (![manage fileExistsAtPath:DirectoryPath])
    {
        // 不存在这个文件夹时进行创建文件夹
        [manage createDirectoryAtPath:DirectoryPath withIntermediateDirectories:YES attributes:nil error:nil];
    }
    
    if (aFileName)
    {
        NSString * filePath = [NSString stringWithFormat:@"%@/%@",DirectoryPath,aFileName];
        
        if (![manage fileExistsAtPath:filePath])
        {
            // 本地不存在这个文件时，进行创建
            [manage createFileAtPath:filePath contents:nil attributes:nil];
        }
        
        return filePath;
    }
    
    return DirectoryPath;
}


